// Exercícios 7 - if / else if / else

// Dada a nota:

// 9

// Mostre:

// >=9 Excelente
// >=7 Bom
// >=5 Recuperação

// Abaixo de 5 Reprovado

let nota = 1

if (nota >=9) {
  console.log(`Exelente`);
} 
else if (nota >=7) {
  console.log(`Bom`);
} 
else if (nota >=5) {
  console.log("Recuperação");
} 
else  {
  console.log(`Reprovado`);
}
